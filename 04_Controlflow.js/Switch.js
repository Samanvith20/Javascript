//Switch
//Switch is a control flow statement that allows a value to change control flow.
//++++++++++++++++++++++++Example+++++++++++++++++++++++++
 let day="wedenseday"
  switch (day) {
    case "monday":
        console.log("Today is Monday");
        break;
    case "tuesday":
        console.log("Today is Tuesday");
        break;
    case "wedenseday":
        console.log("Today is Wedenseday");
        break;
    default:
         console.log("Today is not a Sunday");
        break;
  }